//Comentarios:Hacer un programa para mostrar por pantalla los números del 20 al 1 en orden decreciente.

//Importante: El programa no tiene ningún ingreso de datos.

#include<iostream>
using namespace std;

int main(void){
	 for (int i = 20; i >= 1; i--) {
        cout << i << " ";
    }

    cout << endl;
	return 0;
}

#include<iostream>
using namespace std;

int main(){
	int a = 0, b, c;
	//post-incremento
	a++;
	cout << "Nuevo valor de a (a++): "<< a<< endl;
	//pre incremento
	++a;
	cout << "Nuevo valor de a (++a): "<< a<< endl;

	//op. decremento
	a--;
	cout << "Nuevo valor de a (a--): "<< a<< endl;
	--a;
	cout << "Nuevo valor de a (--a): "<< a<< endl;
	return 0;
}
